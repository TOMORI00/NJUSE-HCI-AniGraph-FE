<script>
export default {
  name: "KgSearchLine",
  data() {
    return {
      content: "",
      display: false,
      switchTip: "搜索",
    };
  },
  props: {
    totalNum: Number,
    currentIndex: Number,
  },
  methods: {
    handleSwitchClick() {
      this.display = !this.display;
      if (this.display) {
        this.switchTip = "隐藏";
      } else {
        this.switchTip = "搜索";
      }
      this.$emit("display-switch", this.display);
    },

    emitSearchEvent() {
      if (this.content !== "") {
        this.$emit("search-acknowledge", this.content);
      }
    },

    emitSearchCancelEvent() {
      this.$emit("search-cancel");
    },

    emitNextResultEvent() {
      this.$emit("next-result");
    },

    emitPrevResultEvent() {
      this.$emit("prev-result");
    },

    setContent(content) {
      this.content = content;
    }
  }
};
</script>

